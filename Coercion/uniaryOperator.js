//Unary Plus (+) -> Converts its operand into a number.
console.log(+"42");   // 42
console.log(+"3.14"); // 3.14
console.log(+true);   // 1
console.log(+false);  // 0
console.log(+null);   // 0
console.log(+undefined); // NaN
console.log(+"Hello"); // NaN

//Unary Minus (-) -> Converts its operand into a number and negates it.
console.log(-"42");   // -42
console.log(-true);   // -1
console.log(-false);  // 0
console.log(-null);   // 0
console.log(-"hello"); // NaN

